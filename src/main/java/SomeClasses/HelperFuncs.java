package SomeClasses;

public class HelperFuncs {




    /*
    Nombre: compareWord
    Funcion: Comparar 2 palabras
    Return: true si son iguales else false
     */
    public boolean compareWord(String word, String wordToCompare)
    {
       return word.compareTo(wordToCompare) == 0;
    }
}
